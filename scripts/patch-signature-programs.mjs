async function patchPrograms() {
  const mutations = PROGRAMS.map((p) => ({
    patch: {
      id: p._id,
      set: {
        'slug.current': p.slug,
        'slug._type': 'slug',
        description: p.description,
        composition: p.composition,
      },
    },
  }));

  const url = `https://${PROJECT_ID}.api.sanity.io/v2024-01-01/data/mutate/${DATASET}`;

  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${TOKEN}`,
    },
    body: JSON.stringify({ mutations }),
  });

  const json = await res.json();
  if (!res.ok) {
    console.error('ERROR:', JSON.stringify(json, null, 2));
    process.exit(1);
  }
  console.log('SUCCESS:', JSON.stringify(json, null, 2));
}

patchPrograms().catch(console.error);
